package com.sesami.sesamiscdmintegration.cbd.transactionPostingBean;

public class RefData {
	private String RefIdent;
    private String CreditRefDesc;
    private String DebitRefDesc;
	public String getRefIdent() {
		return RefIdent;
	}
	public void setRefIdent(String refIdent) {
		RefIdent = refIdent;
	}
	public String getCreditRefDesc() {
		return CreditRefDesc;
	}
	public void setCreditRefDesc(String creditRefDesc) {
		CreditRefDesc = creditRefDesc;
	}
	public String getDebitRefDesc() {
		return DebitRefDesc;
	}
	public void setDebitRefDesc(String debitRefDesc) {
		DebitRefDesc = debitRefDesc;
	}
    
    
}
